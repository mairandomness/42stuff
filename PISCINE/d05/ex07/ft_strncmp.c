int		ft_strncmp(char *s1, char *s2, int n)
{
	while (*s1 != '\0' && *s2 != '\0' && n > 0)
	{
		if (*s1 > *s2)
			return (1);
		if (*s1 < *s2)
			return (-1);
		s1++;
		s2++;
		n--;
	}
	if (*s2 == *s1)
		return (0);
	else if (*s1 > *s2)
		return (1);
	else
		return (-1);
}
